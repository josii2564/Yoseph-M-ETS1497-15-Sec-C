def get_list_length(input_list):
    """
    This function takes a list as input and returns its length using the len() method.
    """
    return len(input_list)

# Example usage
if __name__ == "__main__":
    my_list = ["apple", "banana", "cherry", "date"]
    print("List:", my_list)
    print("Length of the list:", get_list_length(my_list))
